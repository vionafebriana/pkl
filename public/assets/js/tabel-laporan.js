$('#belumsetuju').click((e) => {
    $('#setuju').removeClass('active')
    $('#deaktif').removeClass('active')
    $(e.target).addClass('active')
    $(e.target).css('background-color', '#508b90')
    $('#setuju').css('background-color', '#fff')
    $('#deaktif').css('background-color', '#fff')
    $('#tabel-belumsetuju').css('display', 'block')
    $('#tabel-setuju').css('display', 'none')
    $('#tabel-deaktif').css('display', 'none')

})
